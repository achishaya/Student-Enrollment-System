package nsbmstudentenrollmentsystem;

/**
 *
 * @author Achintha
 */
public class PostgraduateDetails {
    private int uid;
    private String iname;
    private String fname;
    private String address;
    private String gender;
    private int dob;
    private int mobile;
    private String email;
    private String ugdegree;
    private String institute;
    private int year;
    private String faculty;

    /**
     * @return the uid
     */
    public int getUid() {
        return uid;
    }

    /**
     * @param uid the uid to set
     */
    public void setUid(int uid) {
        this.uid = uid;
    }

    /**
     * @return the iname
     */
    public String getIname() {
        return iname;
    }

    /**
     * @param iname the iname to set
     */
    public void setIname(String iname) {
        this.iname = iname;
    }

    /**
     * @return the fname
     */
    public String getFname() {
        return fname;
    }

    /**
     * @param fname the fname to set
     */
    public void setFname(String fname) {
        this.fname = fname;
    }

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return the gender
     */
    public String getGender() {
        return gender;
    }

    /**
     * @param gender the gender to set
     */
    public void setGender(String gender) {
        this.gender = gender;
    }

    /**
     * @return the dob
     */
    public int getDob() {
        return dob;
    }

    /**
     * @param dob the dob to set
     */
    public void setDob(int dob) {
        this.dob = dob;
    }

    /**
     * @return the mobile
     */
    public int getMobile() {
        return mobile;
    }

    /**
     * @param mobile the mobile to set
     */
    public void setMobile(int mobile) {
        this.mobile = mobile;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the ugdegree
     */
    public String getUgdegree() {
        return ugdegree;
    }

    /**
     * @param ugdegree the ugdegree to set
     */
    public void setUgdegree(String ugdegree) {
        this.ugdegree = ugdegree;
    }

    /**
     * @return the institute
     */
    public String getInstitute() {
        return institute;
    }

    /**
     * @param institute the institute to set
     */
    public void setInstitute(String institute) {
        this.institute = institute;
    }

    /**
     * @return the year
     */
    public int getYear() {
        return year;
    }

    /**
     * @param year the year to set
     */
    public void setYear(int year) {
        this.year = year;
    }

    /**
     * @return the faculty
     */
    public String getFaculty() {
        return faculty;
    }

    /**
     * @param faculty the faculty to set
     */
    public void setFaculty(String faculty) {
        this.faculty = faculty;
    }
}
